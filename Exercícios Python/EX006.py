# Exercício da aula: Crie um algoritimo que leia um número e mostre o seu dobro, triplo e raiz quadrada

número = int(input('Digite um número: '))

dobro = número*2
triplo = número*3
raiz = número**(1/2)
print('Analisando o seu número... Descobrir que o dobro dele é {}, o triplo é {}, já a raiz quadrada é {}'.format(dobro, triplo, raiz))